#include <stdio.h>

// Program 3a: Arithmetic Operators
int main() {
    int a = 10, b = 4;
    
    printf("Addition: %d\n", a + b);
    printf("Subtraction: %d\n", a - b);
    printf("Multiplication: %d\n", a * b);
    printf("Division: %d\n", a / b);
    printf("Modulus: %d\n", a % b);
    
    return 0;
}

/* Output:
Addition: 14
Subtraction: 6
Multiplication: 40
Division: 2
Modulus: 2
*/
